        <p>Les dinosaures forment un super-ordre extrêmement diversifié de vertébrés diapsides. Ce sont des archosaures ovipares, ayant en commun une posture érigée et partageant un certain nombre de synapomorphies telles que la présence d'une crête deltopectorale allongée au niveau de l'humérus et un acetabulum perforant le bassin. Présents dès le milleu du Ladinien (dernier étage du Trias moyen), il y a environ 240 Ma, ils disparaissent presque entièrement lors de l'extinction Crétacé-Tertiaire il y a 66 Ma. La majorité des paléontologues admettent que les oiseaux ont émergé de petits dinosaures théropodes du Jurassique supérieur. Ainsi la survie des oiseaux à cette extinction, leur diversification considérable durant le Cénozoïque, en fait le seul groupe vivant actuel de dinosaures selon cette hypothèse. </p>
<p>Ce groupe de vertébrés majoritairement terrestres connut un succès évolutif considérable au Mésozoïque, dominant les faunes continentales entre le Trias supérieur et le Crétacé supérieur pendant plus de 170 Ma. Présents sur l'ensemble des continents dès la fin du Trias, ils comprennent des formes très diverses d'animaux terrestres et volants, bipèdes et quadrupèdes, carnivores et herbivores, ayant développé toute une série d'innovations squelettiques et tégumentaires telles que des cornes, des crêtes, des plaques et des plumes. Les dinosaures non-aviens comptent parmi eux les animaux les plus grands et les plus lourds ayant existé sur la terre ferme. Néanmoins, un grand nombre de dinosaures ne dépassait pas la taille d'un être humain et certains d'entre eux étaient plus petits qu'une poule. </p>
<p>La classification standard des dinosaures distingue deux grands clades selon la morphologie de leur bassin : les Ornithischia et les Saurischia. Les Ornithischia (ou Ornithischiens) ne comprennent que des dinosaures herbivores que les paléontologues divisent en trois groupes majeurs, les Ornithopoda qui regroupent des dinosaures majoritairement bipèdes dont les fameux dinosaures à « bec de canard » (ou Hadrosauridae), les Marginocephalia qui incluent des dinosaures à collerette et à dôme osseux sur le haut de la tête (respectivement les Ceratopsia et les Pachycephalosauria), et enfin les Thyreophora qui englobent des dinosaures quadrupèdes surmontés d'armures, de piques et de plaques osseuses sur le dos et la queue (les Ankylosauria et les Stegosauria). Les Saurischia (ou Saurischiens) sont divisés en deux clades bien distincts, les Theropoda qui comprennent des dinosaures bipèdes et l'entièreté des dinosaures carnivores et piscivores ainsi que les dinosaures à plumes et les dinosaures volants (les oiseaux dans leur grande majorité), et les Sauropodomorpha, des animaux généralement quadrupèdes et de très grande taille munis d'un long cou, d'une petite tête et d'une longue queue. </p>
<p>En 2017, une révision de cette classification est proposée par Matthew Baron et ses collègues, en regroupant d'une part les théropodes et les ornithischiens au sein d'un même clade (les ornithoscélidésa) et d'autre part les sauropodomorphes et les herrérasauridés au sein du clade des saurischiens (qui n'inclut donc plus les théropodes)</p>
